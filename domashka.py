import pandas as pd
import numpy as np


# Создаем DataFrame с данными
data = {
    'Имя': ['Алексей', 'Борис', 'Виктория', 'Галина', 'Дмитрий', 'Елена', 'Жанна', 'Игорь', 'Ксения', 'Леонид'],
    'Математика': [5, 4, 3, 4, 5, 2, 4, 5, 3, 4],
    'Физика': [4, 3, 4, 5, 4, 5, 3, 4, 5, 3],
    'Химия': [3, 4, 5, 4, 3, 4, 5, 3, 4, 5],
    'Биология': [5, 5, 4, 3, 4, 3, 5, 4, 3, 4],
    'Информатика': [4, 3, 5, 4, 3, 4, 3, 5, 4, 3]
}

df = pd.DataFrame(data)

# Выводим первые несколько строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Вычисляем среднюю оценку по каждому предмету
mean_grades = df.iloc[:, 1:].mean()

# Выводим средние оценки
print("\nСредние оценки по каждому предмету:")
print(mean_grades)

# Вычисляем медианную оценку по каждому предмету
median_grades = df.iloc[:, 1:].median()

# Выводим медианные оценки
print("\nМедианные оценки по каждому предмету:")
print(median_grades)

# Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)

# Вычисляем IQR
IQR_math = Q3_math - Q1_math

# Выводим Q1, Q3 и IQR
print("\nПервый квартиль (Q1) для оценок по математике: ", Q1_math)
print("Третий квартиль (Q3) для оценок по математике: ", Q3_math)
print("Межквартильный размах (IQR) для оценок по математике: ", IQR_math)

# Вычисляем стандартное отклонение по каждому предмету
std_deviation = df.iloc[:, 1:].std()

# Выводим стандартное отклонение
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
